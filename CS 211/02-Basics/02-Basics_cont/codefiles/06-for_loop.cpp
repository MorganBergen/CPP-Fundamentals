#include <iostream>

int main()
{
    for (int counter = 10; counter > 0; counter--)
        std::cout << counter << '\n';

    /*
     * General form of for loop
     * for ([COUNTING VARIABLE INITIALIZATION]; [BOOLEAN EXPRESSION]; [UPDATE COUNTING VARIABLE]) {
     *      [Statement(s)]
     * }
     */

    return 0;
}